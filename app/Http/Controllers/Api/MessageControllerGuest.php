        $data = $request->all();

        $request->validate(
            [
                'name' => 'required|max:100',
                'surname' => 'required|max:100',
                'email' => 'required|max:50',
                'message' => 'required|max:400',
                'profile_id' => 'required'
            ]
        );

        $new_message = new Message();
        $new_message->fill($data);
